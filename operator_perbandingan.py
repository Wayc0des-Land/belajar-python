# Operator Perbandingan
#
# > lebih dari
# < kurang dari
# >= lebih dari sama dengan
# <= kurang dari sama dengan
# == sama dengan (bisa digunakan untuk perbandingan teks atau string)
# != tidak sama dengan (bisa digunakan untuk perbandingan teks atau string)

print(1 <= 4) # akan menghasilkan boolean (TRUE atau FALSE)
print(10 == 10)
print(10 != 9)

print("aditra" == "aditra")
print("aditra" != "aditra")